numero = int(input("Digite um número: "))

while numero < 100:
    print("\t" + str(numero))
    numero = numero+1
print("Laço encerrado....")

# ("While") Ele é semelhante ao "if", você dá uma condição a ele e ele vai executar esse código até que a condição seja verificada, deixe
# de ser verdadeira. A partir do momento que ela se tornar falsa, ela para de executar.
